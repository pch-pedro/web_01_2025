let a: number = 10;
let b: number = 5;

console.log(`Soma: ${a+b}`);
console.log(`Subtração: ${a-b}`);
console.log(`Multiplicação: ${a*b}`);
console.log(`Divisão: ${a/b}`);
console.log(`Módulo: ${a%b}`);
console.log(`Exponenciação: ${a**b}`);
console.log(`Igualde: ${a == b}`); // compara 
console.log(`Igualdade Restrita: ${a === b}`); //Testa se o valor é igual e se o tipo da variável também é

console.log(`Incremento: ${++a}`);
console.log(`Atribuição com Adição: ${a += b}`);